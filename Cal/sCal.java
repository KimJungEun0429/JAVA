package Cal;

public class sCal {

			public int setPlus(int a, int b) {
			
				int c = a + b;
				
				return c;
			}
			
			public int setMinus(int a, int b) {
				
				int c = a - b;
				
				return c;
				
			}
			
			public int setCross(int a, int b) {
			
				int c = a * b;
				
				return c;
				
			}
			
			public double setDivision(double a, double b) {
				
				double c = a / b;
						
				return c;
			}
		
		}

